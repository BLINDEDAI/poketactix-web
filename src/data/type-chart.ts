// Bundled type-effectiveness charts (US-070: architecturally significant decision — see design-decisions.md).
//
// PokéAPI's `past_damage_relations` coverage for pre-Gen-6 charts is incomplete and per-type only,
// which would require N round-trips and still miss the Steel/Ghost/Dark Gen-1→Gen-6 adjustments.
// Per the spec (Open Question #3 resolution) and the prompt, the Frontend Developer is AUTHORISED to
// bundle a pre-Gen-6 chart WITHOUT a new spec cycle. We bundle the FULL chart for both eras here so
// weakness/resistance is correct offline and deterministic; the network is reserved for species/move
// /ability lookups, not the (small, static) type chart.
//
// `CHART[attacker][defender]` = damage multiplier the ATTACKER type deals to the DEFENDER type.
// Only non-1.0 relations are listed; absent pairs default to ×1.

import type { TypeName } from '@/types/battle'

export type Multiplier = 0 | 0.5 | 2

/** A sparse per-attacker map of defender → multiplier (entries omitted == ×1). */
export type AttackRow = Partial<Record<TypeName, Multiplier>>

export type TypeChart = Partial<Record<TypeName, AttackRow>>

